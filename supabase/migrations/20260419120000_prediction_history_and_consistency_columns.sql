-- prediction_history: pre-match model snapshot vs actual result (for team consistency)
create table if not exists public.prediction_history (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  season integer not null,
  match_date date not null,
  team_a_id bigint not null references public.teams (id) on delete cascade,
  team_b_id bigint not null references public.teams (id) on delete cascade,
  predicted_margin double precision,
  actual_margin double precision,
  prediction_error double precision,
  prediction_type text,
  confidence text,
  model_version text not null default '2026.04.19-v1',
  was_pre_match_prediction boolean not null default true,
  match_id bigint references public.matches (id) on delete cascade
);

create index if not exists prediction_history_season_idx on public.prediction_history (season);
create index if not exists prediction_history_match_id_idx on public.prediction_history (match_id);

-- team_consistency: explicit totals/averages (alongside legacy prediction_error total if present)
alter table public.team_consistency
  add column if not exists total_prediction_error double precision;

alter table public.team_consistency
  add column if not exists avg_prediction_error double precision;

-- Best-effort backfill from legacy column name
update public.team_consistency
set total_prediction_error = coalesce(total_prediction_error, prediction_error)
where total_prediction_error is null
  and prediction_error is not null;

update public.team_consistency
set avg_prediction_error = coalesce(
  avg_prediction_error,
  case
    when matches_evaluated > 0 and prediction_error is not null
    then prediction_error / matches_evaluated
    else null
  end
)
where avg_prediction_error is null;
